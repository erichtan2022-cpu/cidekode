  members.map((m, i) => {
    const isFemale = m.gender
      ? m.gender === 'F'
      : m.name.trim().toLowerCase().startsWith('cide');
    const hasPhoto = !!photos[i];
    return {
      name: m.name,
      gelar: m.gelar,
      angkatan,
      photo: hasPhoto ? photos[i] : '',
      hasPhoto,
    };
  });

